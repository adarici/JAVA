package day03increment_decrement_operators;

public class OrderOfOperations01 {

	public static void main(String[] args) {
		/*
		 *Order of operations
		 1) Calculate exponents
		 2) Do the operation inside the parenthesis
		 3) Do multiplication and division. Do them left to right
		 4) Do additions and subtractions again left to right
		 */
		// 38/2-(4+3)*2=?
		System.out.println(38/2-(4+3)*2);

	}

}
